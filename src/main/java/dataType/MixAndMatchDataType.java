package dataType;

public class MixAndMatchDataType {
	/*
	 * we are buying apples (int data type)
	 * and pc ( double data type)
	 * and ink ( float data type)
	 * how much all together( totalSpent)
	 */

	public static void main(String[] args) {
			
	 int apples=5;
	 double pc=999.99;
	 float ink=149.99f;
	 
	 double totalSpent =apples+pc+ink;
	 System.out.print(totalSpent);
	 //between this three data types double is the super data type
	 //Primitive full number data type= byte, short, int, long
	//Primitive decimal number data type= float, double
	
	
	}

}
